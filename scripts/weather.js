  get minutesContext() {
    let minutesGraph = '';
    let height;
    const width = 100 / this.#data.minutely.length;
    let isNonePrecipitation = true;

    this.#data.minutely.forEach((element) => {
      if (element.precipitation > 0) {
        isNonePrecipitation = false;
      }

      if (element.precipitation > 15) {
        height = 100;
      } else {
        height = (100 * element.precipitation) / 15;
      }

      minutesGraph += `<div style="margin-right:1px; background-color: white; width:calc(${width}% - 1px); height:${height}%; "></div>`;
    });

    if (isNonePrecipitation) {
      return `Осадков в ближайшие ${
        this.#data.minutely.length - 1
      } минут не ожидается`;
    }
    return minutesGraph;
  }

