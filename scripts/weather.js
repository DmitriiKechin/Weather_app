  #getIcon(weatherID, time, timeSunset, timeSunrise) {
    const item = weatherID;
    let dayOrNight;

    if (timeSunset > time && time > timeSunrise) {
      dayOrNight = 'd';
    } else {
      dayOrNight = 'n';
    }

    if (item + dayOrNight in Weather.#iconBase) {
      return Weather.#iconBase[item + dayOrNight];
    } else if (item in Weather.#iconBase) {
      return Weather.#iconBase[item];
    } else {
      return Weather.#iconBase[item[0] + '00default'];
    }
  }

  #timeSetRise(timeHour) {
    const time = new Date(
      (timeHour + this.#data.timezone_offset) * 1000
    ).getUTCDate();
    let timeDay;

    for (const day of this.#data.daily) {
      timeDay = new Date(
        (day.dt + this.#data.timezone_offset) * 1000
      ).getUTCDate();

      if (timeDay === time) {
        return [day.sunset, day.sunrise];
      }
    }
  }

